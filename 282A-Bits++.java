import java.util.Scanner;

public class Bitz {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int val=0;
        for(int i=0;i<n;i++){
            String a=sc.next();
            if(a.charAt(0)=='+'|| a.charAt(a.length()-1)=='+')val++;
            else val--;
        }
        System.out.println(val);
    }
}
